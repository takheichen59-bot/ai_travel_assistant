import json


SYSTEM_PROMPT = (
    "你是一个专业的中文本地生活与出行推荐助手。"
    "你的任务是把混乱的搜索结果清洗成用户真正能用的推荐。"
    "你不能直接复制网页原文。"
    "你不能把垃圾搜索结果堆给用户。"
    "你要根据城市、天气、出发地点和地点类型，选出最适合的一个地点，并说明为什么、怎么去、怎么玩。"
)


def build_llm_prompt(
    city: str,
    weather: str,
    start_location: str,
    place_type: str,
    search_results: list,
) -> str:
    data = {
        "用户需求": {
            "城市": city,
            "天气": weather,
            "出发地点": start_location if start_location.strip() else "用户未填写，默认从市中心或当前位置出发",
            "想去的地方类型": place_type,
        },
        "搜索结果": search_results,
    }

    lines = [
        "下面是用户需求和搜索引擎返回的资料。",
        "请你从搜索结果中筛选有用信息，过滤掉垃圾网页、交通工具网页、酒店广告、过长旅游攻略和无关内容。",
        "",
        "重要要求：",
        "1. 只推荐 1 个最适合的地点。",
        "2. 如果有 1-2 个不错的备选，可以简短列出。",
        "3. 不要复制搜索结果原文。",
        "4. 不要输出大段网页内容。",
        "5. 输出必须是简体中文。",
        "6. 结构要清楚，适合显示在网页上。",
        "7. 如果你不确定具体公交或地铁路线，不要编造线路。",
        "8. 交通部分可以给出大方向，并提醒用户用地图 App 确认实时路线。",
        "9. 回答要实用，不要空泛。",
        "",
        "请严格按照下面结构输出：",
        "",
        "## 📍 推荐地点",
        "地点名称：...",
        "",
        "## ✅ 为什么推荐",
        "- ...",
        "- ...",
        "- ...",
        "",
        "## 🧭 怎么去",
        "- 出发地：...",
        "- 建议交通方式：...",
        "- 路线提醒：...",
        "- 实时确认：建议用高德地图、百度地图或 Google Maps 确认。",
        "",
        "## 🎒 适合怎么玩",
        "- ...",
        "- ...",
        "",
        "## ⏰ 建议停留时间",
        "...",
        "",
        "## ⚠️ 注意事项",
        "- ...",
        "- ...",
        "",
        "## 🔁 备选地点",
        "- 如果有合适备选，列出 1-2 个；没有就写“暂无特别合适的备选”。",
        "",
        "下面是输入数据：",
        json.dumps(data, ensure_ascii=False, indent=2),
    ]

    return "\n".join(lines)